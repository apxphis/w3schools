public class ifelse {
  public static void main(String[] args) {
    int x = 20, y = 10;
    if (x < y) {
      System.out.println("x is greater than y");
    } else if (x > y) {
      System.out.println("y is greater than x");
    } else {
      System.out.println("something went wrong!");
    }
  }
}
