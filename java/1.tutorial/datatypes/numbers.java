public class datatypes {
  public static void main(String[] args) {
    byte myBite = 100; // a byte can store a number that is between -128 and 127
    System.out.println(myBite);
    short myShorts = 5000; // a short can store a number that is between -32768 to 32767
    System.out.println(myShorts);
    int myInteger = 100000; // an integer can store a number that is between -2147483648 to 2147483647
    System.out.println(myInteger);
    long myLong = 15000000000L; // a long variable can store a number that is between -9223372036854775808 to 9223372036854775807
    System.out.println(myLong);
    float myFloaty = 9.99f; // a floating point number can store a fractional numbers with up to 6 or 7 digits
    System.out.println(myFloaty);
    double myDub = 19.99d; // a double can store a fractional number with up to 15 digits
    System.out.println(myDub);
  }
}
