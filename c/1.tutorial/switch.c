#include <stdio.h>

int main() {
  int day = 5;
  switch (day) {
    case 1:
      printf("it is monday!");
      break;
    case 2:
      printf("it is tuesday!");
      break;
    case 3:
      printf("it is wednesday!");
    case 4:
      printf("it is thursday");
      break;
    case 5:
      printf("it is friday!");
  }
}
